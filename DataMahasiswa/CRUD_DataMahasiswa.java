    private void load_table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn ("Nama");
        model.addColumn ("NIM");
        model.addColumn ("Jurusan");
        model.addColumn ("Alamat");
        model.addColumn ("Kontak");
        model.addColumn ("Fakultas");
        model.addColumn ("Universitas");
        model.addColumn ("Minat");
        
        try {
            String sql = "select * from tmahasiswa";
            java.sql.Connection conn=(Connection) Config.configDB();
            java.sql.Statement stm= conn.createStatement();
            java.sql.ResultSet rslt= stm.executeQuery (sql);
            while (rslt.next()) {
                model.addRow(new Object[] {rslt.getString(1),
                    rslt.getString(2), rslt.getString(3), rslt.getString(4), rslt.getString(5), rslt.getString(6),
                    rslt.getString(7), rslt.getString(8)});
            }
            tmahasiswa.setModel(model);
        } catch (Exception e) {
        }    
    }
    
