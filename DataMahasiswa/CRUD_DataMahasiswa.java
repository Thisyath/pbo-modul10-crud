    private void tmahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tmahasiswaMouseClicked
     int baris = tmahasiswa.rowAtPoint(evt.getPoint());
     if (tmahasiswa.getValueAt(baris, 0)== null){
         Nama.setText("");
     }else{
         Nama.setText(tmahasiswa.getValueAt(baris, 0) .toString());
     }
     if (tmahasiswa.getValueAt(baris, 1)== null){
         NIM.setText("");
     }else{
         NIM.setText(tmahasiswa.getValueAt(baris, 1).toString());
     } 
     if (tmahasiswa.getValueAt(baris, 2)== null){
         Jurusan.setText("");
     }else{
         Jurusan.setText(tmahasiswa.getValueAt(baris, 2).toString());
     } 
     if (tmahasiswa.getValueAt(baris, 3)== null){
         Alamat.setText("");
     }else{
         Alamat.setText(tmahasiswa.getValueAt(baris, 3).toString());
     } 
      if (tmahasiswa.getValueAt(baris, 4)== null){
         Kontak.setText("");
     }else{
         Kontak.setText(tmahasiswa.getValueAt(baris, 4).toString());
     } 
     if (tmahasiswa.getValueAt(baris, 5)== null){
         Fakultas.setText("");
     }else{
         Fakultas.setText(tmahasiswa.getValueAt(baris, 5).toString());
     } 
     if (tmahasiswa.getValueAt(baris, 6)== null){
         Universitas.setText("");
     }else{
         Universitas.setText(tmahasiswa.getValueAt(baris, 6).toString());
     } 
     if (tmahasiswa.getValueAt(baris, 7)== null){
         Minat.setText("");
     }else{
         Minat.setText(tmahasiswa.getValueAt (baris, 7) .toString());
     }    
    }//GEN-LAST:event_tmahasiswaMouseClicked

